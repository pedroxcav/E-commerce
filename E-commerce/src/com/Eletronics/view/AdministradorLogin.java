    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        // TODO add your handling code here:
        String ID_Administrador = Campo_ID.getText();
        String senha = String.valueOf(Campo_Senha.getPassword());
        
        Administrador administrador = new Administrador();
        if (ID_Administrador.isEmpty() || senha.isEmpty()) {
            Warning warning = new Warning("Dados incorretos.");
            warning.setVisible(true);
        } else if (administrador.entrar(ID_Administrador, senha)) {
            administrador = AdministradorServices.trazerAdministrador(ID_Administrador);
            AdministradorMain administradorMain = new AdministradorMain(administrador);
            administradorMain.setVisible(true);
            this.dispose();
        } else {
            Warning warning = new Warning("Acesso incorreto.");
            warning.setVisible(true);
        }
        Campo_ID.setText("");
        Campo_Senha.setText("");
    }//GEN-LAST:event_entrarActionPerformed

