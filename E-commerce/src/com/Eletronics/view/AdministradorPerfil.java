    private void atualizarDados() throws Exception_Cadastro {
        String nome = Campo_Nome.getText();
        String ID_Administrador = Campo_ID.getText();
        String senha = Campo_Senha.getText();
        if (!nome.equals(administrador.getNome()) || !ID_Administrador.equals(administrador.getID()) || !senha.equals(administrador.getSenha())) {
            AdministradorServices.atualizar(new Administrador(ID_Administrador, nome, administrador.getCPF(), senha));
            administrador.setNome(nome);
            Texto_Nome.setText(nome.toUpperCase());
            administrador.setID(ID_Administrador);
            administrador.setSenha(senha);
        }
    }

