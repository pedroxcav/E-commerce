    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        // TODO add your handling code here:
        String nome = Campo_Nome.getText();
        String ID_Administrador = Campo_ID.getText();
        String CPF = Campo_CPF.getText();
        String senha = Campo_Senha.getText();
        
        if (nome.isEmpty() || ID_Administrador.isEmpty() || CPF.isEmpty() || CPF.length() != 11 || senha.isEmpty()) {
            Warning warning = new Warning("Dados incorretos.");
            warning.setVisible(true);
        } else {
            Administrador administrador = new Administrador(ID_Administrador, nome, CPF, senha);
            administrador.registar(administrador);
            Campo_Lista.setModel(AdministradorServices.trazerAdministradores());
        }
        Campo_Nome.setText("");
        Campo_ID.setText("");
        Campo_CPF.setText("");
        Campo_Senha.setText("");
    }//GEN-LAST:event_cadastrarActionPerformed

