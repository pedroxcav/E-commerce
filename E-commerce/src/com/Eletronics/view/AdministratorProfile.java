    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        try {
            if (editable == false){
                nameField.setEditable(true);
                userIdField.setEditable(true);
                passwordField.setEditable(true);
                editButton.setBackground(Color.WHITE);
                editButton.setForeground(Color.BLACK);
                editButton.setText("SALVAR");
                editable = true;
            } else {
                String name = nameField.getText();
                String userId = userIdField.getText();
                String password = passwordField.getText();
                if (name.isEmpty() || userId.isEmpty() || password.isEmpty()) {
                    Warning warning = new Warning("Informe os dados corretamente!");
                    warning.setVisible(true);
                } else {
                    if (!name.equals(administrator.getName()) || !userId.equals(administrator.getUserId()) || !password.equals(administrator.getPassword())) {
                        AdministratorServices.updateUser(new Administrator(name,userId,administrator.getCPF(),password));
                        administrator.setName(name);
                        nameText.setText(administrator.getName().toUpperCase());
                        administrator.setUserId(userId);
                        administrator.setPassword(password);
                    }
                    nameField.setEditable(false);
                    userIdField.setEditable(false);
                    passwordField.setEditable(false);
                    editButton.setBackground(Color.BLACK);
                    editButton.setForeground(Color.WHITE);
                    editButton.setText("EDITAR");
                    editable = false;
                }
            }
        } catch (Exception_Data e) {
            Warning warning = new Warning(e.getMessage());
            warning.setVisible(true);
        }
    }//GEN-LAST:event_editButtonActionPerformed

