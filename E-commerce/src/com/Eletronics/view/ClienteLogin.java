    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        // TODO add your handling code here:
        String ID_Cliente = Campo_ID.getText();
        String senha = String.valueOf(Campo_Senha.getPassword());
        
        Cliente cliente = new Cliente();
        if (ID_Cliente.isEmpty() || senha.isEmpty()) {
            Warning warning = new Warning("Dados incorretos.");
            warning.setVisible(true);
        } else if (cliente.entrar(ID_Cliente, senha)) {
            cliente = ClienteServices.trazerCliente(ID_Cliente);
            ClienteMain clienteMain = new ClienteMain(cliente);
            clienteMain.setVisible(true);
            this.dispose();
        } else {
            Warning warning = new Warning("Acesso incorreto.");
            warning.setVisible(true);
        }
        Campo_ID.setText("");
        Campo_Senha.setText("");
    }//GEN-LAST:event_entrarActionPerformed

