    private void adicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarActionPerformed
        // TODO add your handling code here:
         if (!Campo_Quantidade.getText().equals("Selecionar")) {
             Carrinho carrinho = CarrinhoServices.trazerCarrinho(cliente);
             Pedido pedido = new Pedido(carrinho.getID_Carrinho(), produto, Integer.parseInt(Campo_Quantidade.getText()));
             PedidoServices.salvar(pedido);
             Campo_Quantidade.setText("Selecionar");
        } else {
            Warning warning = new Warning("Selecione uma quantidade!");
            warning.setVisible(true); 
        }
    }//GEN-LAST:event_adicionarActionPerformed

