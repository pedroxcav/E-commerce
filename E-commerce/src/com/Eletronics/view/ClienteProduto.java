    private void selecionarProduto(String action) {
        DefaultListModel listModel = ProdutoServices.trazerProdutos();
        if (action.equals("proximo")) {
            indice++;
            if (indice > listModel.size()-1) indice = 0;
        } else if (action.equals("anterior")) {
            indice--;
            if (indice < 0) indice = listModel.size() - 1;
        }
        produto = (Produto)listModel.getElementAt(indice);
        Campo_Nome.setText(produto.getNome().toUpperCase());
        Campo_ID.setText(produto.getID_Produto());
        Campo_Descricao.setText(produto.getDescricao());
        Campo_Valor.setText("R$ "+produto.getValor());
        Campo_Imagem.setIcon(new ImageIcon(produto.getImagem()));
        Campo_Quantidade.setText("Selecionar");
    }

